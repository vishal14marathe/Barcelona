// Helper function to generate random chart data
function generateChartData(baseLevel) {
  const months = [
    "Jan",
    "Feb",
    "Mar",
    "Apr",
    "May",
    "Jun",
    "Jul",
    "Aug",
    "Sep",
    "Oct",
    "Nov",
    "Dec",
  ];
  return months.map((month) => ({
    month,
    crime2024: Math.floor(baseLevel + Math.random() * 20),
    crime2025: Math.floor(baseLevel * 0.8 + Math.random() * 15),
  }));
}

// Add countries to locations
countryData.forEach((country) => {
  const statusIndex = Math.floor(Math.random() * 3);
  const baseCrimeLevel = (3 - statusIndex) * 8 + Math.random() * 15;
  const safetyStatuses = ["Extremely Safe", "Very Safe", "Generally Safe"];

  locations.push({
    name: country,
    slug: country.toLowerCase().replace(/\s+/g, "-"),
    type: "country",
    country: country,
    safetyStatus: safetyStatuses[statusIndex],
    rating: parseFloat((8.0 + Math.random() * 1.5).toFixed(1)),
    description: `${country} is ${safetyStatuses[
      statusIndex
    ].toLowerCase()} for travelers with excellent infrastructure and security.`,
    risks: [
      "General travel precautions advised.",
      "Respect local laws and customs.",
      "Emergency services widely available.",
    ],
    chartData: generateChartData(baseCrimeLevel),
  });
});

export default locations;
